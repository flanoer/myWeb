public class CommentVO {

	/** 코멘트의 고유id */
	private String comment_id;

	/** 코멘트가 달린 원글의 id */
	private String orirec_id;

	/** 내용 */
	private String cmt_content;

	/** 등록자 */
	private String regi_id;

	/** 등록자 */
	private String regidate;

	
	
	public String getComment_id() {
		return comment_id;
	}

	public void setComment_id(String comment_id) {
		this.comment_id = comment_id;
	}

	public String getOrirec_id() {
		return orirec_id;
	}

	public void setOrirec_id(String orirec_id) {
		this.orirec_id = orirec_id;
	}

	public String getCmt_content() {
		return cmt_content;
	}

	public void setCmt_content(String cmt_content) {
		this.cmt_content = cmt_content;
	}

	public String getRegi_id() {
		return regi_id;
	}

	public void setRegi_id(String regi_id) {
		this.regi_id = regi_id;
	}

	public String getRegidate() {
		return regidate;
	}

	public void setRegidate(String regidate) {
		this.regidate = regidate;
	}

}
